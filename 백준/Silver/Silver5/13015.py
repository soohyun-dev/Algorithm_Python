N=int(input())
print('*'*N+' '*(2*N-3)+'*'*N)
tmp=((N-2)*2)-1
cnt=0
for i in range(2,N):
    print(' '*(i-1)+'*'+' '*(N-2)+'*'+' '*(tmp-(2*cnt))+'*'+' '*(N-2)+'*'+' ')
    cnt+=1

print(' '*(N-1)+'*'+' '*(N-2)+'*'+' '*(N-2)+'*'+' '*1)
    
for j in range(N-1,1,-1):
    cnt-=1
    print(' '*(j-1)+'*'+' '*(N-2)+'*'+' '*(tmp-(2*cnt))+'*'+' '*(N-2)+'*'+' ')
print('*'*N+' '*(2*N-3)+'*'*N)